package model;

public class DoAn{
    private String maDoAn;
    private String tenDoAn;
    private int gia; // tinh bang Viet Nam Dong
    private int soLuong;

    public DoAn(){};

    public DoAn(String maDoAn, String tenDoAn, int gia, int soLuong){
        this.maDoAn = maDoAn;
        this.tenDoAn = tenDoAn;
        this.gia = gia;
        this.soLuong = soLuong;
        this.gia = gia;
    }
}