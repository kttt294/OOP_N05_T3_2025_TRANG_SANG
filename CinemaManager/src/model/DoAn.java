package model;

public class DoAn{
    String maDoAn;
    String tenDoAn;
    int gia; // tinh bang Viet Nam Dong
    int soLuong;

    DoAn(){};

    public DoAn(String maDoAn, String tenDoAn, int gia, int soLuong){
        this.maDoAn = maDoAn;
        this.tenDoAn = tenDoAn;
        this.gia = gia;
        this.soLuong = soLuong;
        this.gia = gia;
    }
}